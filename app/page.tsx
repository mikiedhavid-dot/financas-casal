function MonthlyComparisonChart({
  transactions,
}: {
  transactions: Array<{ month: string; value: number; paidBy: string }>;
}) {
  const data = useMemo(() => {
    const map: Record<string, { mikie: number; natalia: number; total: number }> = {};

    transactions.forEach((t) => {
      if (!map[t.month]) {
        map[t.month] = { mikie: 0, natalia: 0, total: 0 };
      }

      if (t.paidBy === "Mikie") map[t.month].mikie += t.value;
      if (t.paidBy === "Natalia") map[t.month].natalia += t.value;
      map[t.month].total += t.value;
    });

    return Object.entries(map)
      .sort()
      .slice(-8)
      .map(([month, values]) => ({ month, ...values }));
  }, [transactions]);

  const max = Math.max(...data.map((d) => d.total), 1);

  return (
    <div style={{ display: "grid", gap: 16 }}>
      <div
        style={{
          display: "flex",
          gap: 16,
          flexWrap: "wrap",
          fontSize: 13,
          color: "#64748b",
        }}
      >
        <span style={{ display: "inline-flex", alignItems: "center", gap: 6 }}>
          <span style={{ width: 10, height: 10, borderRadius: 999, background: "#0f172a" }} />
          Total
        </span>
        <span style={{ display: "inline-flex", alignItems: "center", gap: 6 }}>
          <span style={{ width: 10, height: 10, borderRadius: 999, background: "#475569" }} />
          Mikie
        </span>
        <span style={{ display: "inline-flex", alignItems: "center", gap: 6 }}>
          <span style={{ width: 10, height: 10, borderRadius: 999, background: "#94a3b8" }} />
          Natalia
        </span>
      </div>

      {data.map((item) => {
        const totalPct = (item.total / max) * 100;
        const mikiePct = (item.mikie / max) * 100;
        const nataliaPct = (item.natalia / max) * 100;

        return (
          <div key={item.month} style={{ display: "grid", gap: 8 }}>
            <div
              style={{
                display: "flex",
                justifyContent: "space-between",
                gap: 12,
                alignItems: "center",
              }}
            >
              <strong>{getMonthLabel(item.month)}</strong>
              <span style={{ color: "#64748b", fontSize: 13 }}>
                Total {currency(item.total)}
              </span>
            </div>

            <div style={{ display: "grid", gap: 6 }}>
              <div>
                <div style={{ fontSize: 12, marginBottom: 4 }}>Total</div>
                <div
                  style={{
                    height: 10,
                    background: "rgba(148,163,184,0.18)",
                    borderRadius: 999,
                    overflow: "hidden",
                  }}
                >
                  <div
                    style={{
                      width: `${totalPct}%`,
                      height: "100%",
                      background: "#0f172a",
                      borderRadius: 999,
                    }}
                  />
                </div>
              </div>

              <div>
                <div style={{ fontSize: 12, marginBottom: 4 }}>Mikie</div>
                <div
                  style={{
                    height: 10,
                    background: "rgba(148,163,184,0.18)",
                    borderRadius: 999,
                    overflow: "hidden",
                  }}
                >
                  <div
                    style={{
                      width: `${mikiePct}%`,
                      height: "100%",
                      background: "#475569",
                      borderRadius: 999,
                    }}
                  />
                </div>
              </div>

              <div>
                <div style={{ fontSize: 12, marginBottom: 4 }}>Natalia</div>
                <div
                  style={{
                    height: 10,
                    background: "rgba(148,163,184,0.18)",
                    borderRadius: 999,
                    overflow: "hidden",
                  }}
                >
                  <div
                    style={{
                      width: `${nataliaPct}%`,
                      height: "100%",
                      background: "#94a3b8",
                      borderRadius: 999,
                    }}
                  />
                </div>
              </div>
            </div>
          </div>
        );
      })}

      {!data.length && (
        <div className="inner-card empty-card">Ainda não há dados suficientes para o gráfico.</div>
      )}
    </div>
  );
}