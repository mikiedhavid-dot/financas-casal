function currency(value: number) {
  return new Intl.NumberFormat("pt-BR", {
    style: "currency",
    currency: "BRL",
  }).format(value || 0);
}

function formatDate(date: string) {
  return new Date(`${date}T12:00:00`).toLocaleDateString("pt-BR");
}

function statusLabel(status: BillStatus) {
  if (status === "paga") return "Paga";
  if (status === "atrasada") return "Atrasada";
  return "Pendente";
}

function typeLabel(type: TransactionType) {
  if (type === "mikie") return "Mikie";
  if (type === "natalia") return "Natalia";
  if (type === "casa") return "Casa";
  return "Compartilhado";
}

function getDisplayName(user: User | null) {
  if (!user) return "Visitante";
  return user.displayName?.split(" ")[0] || user.email || "Usuário";
}

function daysUntil(date: string) {
  const today = new Date();
  const base = new Date(today.getFullYear(), today.getMonth(), today.getDate(), 12, 0, 0);
  const target = new Date(`${date}T12:00:00`);
  return Math.ceil((target.getTime() - base.getTime()) / (1000 * 60 * 60 * 24));
}

function buildDateForMonth(month: string, sourceDate: string) {
  const day = sourceDate.slice(8, 10);
  return `${month}-${day}`;
}

function monthDiff(fromMonth: string, toMonth: string) {
  const [fy, fm] = fromMonth.split("-").map(Number);
  const [ty, tm] = toMonth.split("-").map(Number);
  return (ty - fy) * 12 + (tm - fm);
}

function notificationMeta(type: NotificationType, seen: boolean) {
  const dim = seen ? { opacity: 0.6 } : undefined;

  if (type === "gasto") {
    return {
      icon: <Wallet size={16} style={dim} />,
      className: "notif gasto",
    };
  }

  if (type === "conta") {
    return {
      icon: <CheckCircle2 size={16} style={dim} />,
      className: "notif conta",
    };
  }

  if (type === "vencimento") {
    return {
      icon: <CalendarClock size={16} style={dim} />,
      className: "notif vencimento",
    };
  }

  return {
    icon: <Bell size={16} style={dim} />,
    className: "notif sistema",
  };
}

